	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												techanmingcheng: '',
																techanleixing: '',
																tupian: '',
																shengchanriqi: '',
																baozhiqi: '',
																techanxiangqing: '',
																thumbsupnum: '',
																crazilynum: '',
																price: '',
												},
																				techanleixingOptions: [],
				techanleixingIndex: 0,
																																																																				// 登陆用户信息
				user: {},
                                ro:{
                                   techanmingcheng : false,
                                   techanleixing : false,
                                   tupian : false,
                                   shengchanriqi : false,
                                   baozhiqi : false,
                                   techanxiangqing : false,
                                   thumbsupnum : false,
                                   crazilynum : false,
                                   price : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																												
																		// 下拉框
			res = await this.$api.option(`techanleixing`,`techanleixing`,{});
			this.techanleixingOptions = res.data;
																																																			
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`techanxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='techanmingcheng'){
					this.ruleForm.techanmingcheng = obj[o];
					this.ro.techanmingcheng = true;
					continue;
					}
					if(o=='techanleixing'){
					this.ruleForm.techanleixing = obj[o];
					this.ro.techanleixing = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='shengchanriqi'){
					this.ruleForm.shengchanriqi = obj[o];
					this.ro.shengchanriqi = true;
					continue;
					}
					if(o=='baozhiqi'){
					this.ruleForm.baozhiqi = obj[o];
					this.ro.baozhiqi = true;
					continue;
					}
					if(o=='techanxiangqing'){
					this.ruleForm.techanxiangqing = obj[o];
					this.ro.techanxiangqing = true;
					continue;
					}
					if(o=='thumbsupnum'){
					this.ruleForm.thumbsupnum = obj[o];
					this.ro.thumbsupnum = true;
					continue;
					}
					if(o=='crazilynum'){
					this.ruleForm.crazilynum = obj[o];
					this.ro.crazilynum = true;
					continue;
					}
					if(o=='price'){
					this.ruleForm.price = obj[o];
					this.ro.price = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																												
			// 多级联动参数
																																																												
																											shengchanriqiChange(e) {
				this.ruleForm.shengchanriqi = e.target.value;
				this.$forceUpdate();
			},
																																				
																																																									
															// 下拉变化
			techanleixingChange(e) {
				this.techanleixingIndex = e.target.value
				this.ruleForm.techanleixing = this.techanleixingOptions[this.techanleixingIndex]
			},
																																																
																											tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																															
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																if((!this.ruleForm.techanmingcheng)){
					this.$utils.msg(`特产名称不能为空`);
					return
				}
																																												if((!this.ruleForm.techanleixing)){
					this.$utils.msg(`特产类型不能为空`);
					return
				}
																																																																																																																																																																																																if(this.ruleForm.thumbsupnum&&(!this.$validate.isIntNumer(this.ruleForm.thumbsupnum))){
					this.$utils.msg(`赞应输入整数`);
					return
				}
																																												if(this.ruleForm.crazilynum&&(!this.$validate.isIntNumer(this.ruleForm.crazilynum))){
					this.$utils.msg(`踩应输入整数`);
					return
				}
																																								if((!this.ruleForm.price)){
					this.$utils.msg(`价格不能为空`);
					return
				}
																if(this.ruleForm.price&&(!this.$validate.isNumber(this.ruleForm.price))){
					this.$utils.msg(`价格应输入数字`);
					return
				}
																																if(this.ruleForm.id){
					await this.$api.update(`techanxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`techanxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>


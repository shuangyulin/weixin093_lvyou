<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												meishimingcheng: '',
																meishileixing: '',
																meishitupian: '',
																meishijieshao: '',
																meishixiangqing: '',
																thumbsupnum: '',
																crazilynum: '',
																price: '',
												},
																				meishileixingOptions: [],
				meishileixingIndex: 0,
																																																												// 登陆用户信息
				user: {},
                                ro:{
                                   meishimingcheng : false,
                                   meishileixing : false,
                                   meishitupian : false,
                                   meishijieshao : false,
                                   meishixiangqing : false,
                                   thumbsupnum : false,
                                   crazilynum : false,
                                   price : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																						
																		// 下拉框
			res = await this.$api.option(`meishileixing`,`leixing`,{});
			this.meishileixingOptions = res.data;
																																													
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jingdianmeishi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='meishimingcheng'){
					this.ruleForm.meishimingcheng = obj[o];
					this.ro.meishimingcheng = true;
					continue;
					}
					if(o=='meishileixing'){
					this.ruleForm.meishileixing = obj[o];
					this.ro.meishileixing = true;
					continue;
					}
					if(o=='meishitupian'){
					this.ruleForm.meishitupian = obj[o];
					this.ro.meishitupian = true;
					continue;
					}
					if(o=='meishijieshao'){
					this.ruleForm.meishijieshao = obj[o];
					this.ro.meishijieshao = true;
					continue;
					}
					if(o=='meishixiangqing'){
					this.ruleForm.meishixiangqing = obj[o];
					this.ro.meishixiangqing = true;
					continue;
					}
					if(o=='thumbsupnum'){
					this.ruleForm.thumbsupnum = obj[o];
					this.ro.thumbsupnum = true;
					continue;
					}
					if(o=='crazilynum'){
					this.ruleForm.crazilynum = obj[o];
					this.ro.crazilynum = true;
					continue;
					}
					if(o=='price'){
					this.ruleForm.price = obj[o];
					this.ro.price = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																						
			// 多级联动参数
																																																						
																																																			
																																																			
															// 下拉变化
			meishileixingChange(e) {
				this.meishileixingIndex = e.target.value
				this.ruleForm.meishileixing = this.meishileixingOptions[this.meishileixingIndex]
			},
																																										
																											meishitupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.meishitupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																						
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																												if((!this.ruleForm.meishimingcheng)){
					this.$utils.msg(`美食名称不能为空`);
					return
				}
																																																																																																																																																																																																if(this.ruleForm.thumbsupnum&&(!this.$validate.isIntNumer(this.ruleForm.thumbsupnum))){
					this.$utils.msg(`赞应输入整数`);
					return
				}
																																												if(this.ruleForm.crazilynum&&(!this.$validate.isIntNumer(this.ruleForm.crazilynum))){
					this.$utils.msg(`踩应输入整数`);
					return
				}
																																								if((!this.ruleForm.price)){
					this.$utils.msg(`价格不能为空`);
					return
				}
																if(this.ruleForm.price&&(!this.$validate.isNumber(this.ruleForm.price))){
					this.$utils.msg(`价格应输入数字`);
					return
				}
																																if(this.ruleForm.id){
					await this.$api.update(`jingdianmeishi`, this.ruleForm);
				}else{
					await this.$api.add(`jingdianmeishi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
